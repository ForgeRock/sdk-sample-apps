import SwiftUI

/// Enum representing the app's theme mode options.
enum ThemeMode: String, CaseIterable, Identifiable {
    case system = "System"
    case light = "Light"
    case dark = "Dark"

    var id: String { rawValue }

    /// Display name for the theme mode
    var displayName: String {
        rawValue
    }

    /// Convert ThemeMode to SwiftUI ColorScheme
    var colorScheme: ColorScheme? {
        switch self {
        case .system:
            return nil
        case .light:
            return .light
        case .dark:
            return .dark
        }
    }
}
